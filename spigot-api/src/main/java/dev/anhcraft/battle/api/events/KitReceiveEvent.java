package dev.anhcraft.battle.api.events;

import dev.anhcraft.battle.api.Kit;
import org.bukkit.entity.Player;
import org.bukkit.event.Cancellable;
import org.bukkit.event.HandlerList;
import org.bukkit.event.player.PlayerEvent;
import org.jetbrains.annotations.NotNull;

public class KitReceiveEvent extends PlayerEvent implements Cancellable {
    public static final HandlerList handlers = new HandlerList();

    private final Kit kit;
    private boolean cancelled;

    public KitReceiveEvent(@NotNull Player who, @NotNull Kit kit) {
        super(who);
        this.kit = kit;
    }

    @NotNull
    public Kit getKit() {
        return kit;
    }

    @Override
    public boolean isCancelled() {
        return cancelled;
    }

    @Override
    public void setCancelled(boolean cancel) {
        this.cancelled = cancel;
    }

    @Override
    public @NotNull HandlerList getHandlers() {
        return handlers;
    }

    public static HandlerList getHandlerList() {
        return handlers;
    }
}
